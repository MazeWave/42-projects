#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	unit;

	unit = n % 10 + 48;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
		ft_putnbr_fd(n, fd);
	}
	else if (n < 10)
		write(fd, &unit, 1);
	else
	{
		ft_putnbr_fd(n / 10, fd);
		write(fd, &unit, 1);
	}
}
