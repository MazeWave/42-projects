#include "libft.h"

void	ft_bzero(void *b, size_t len)
{
	unsigned char	*g;
	size_t			i;

	i = 0;
	g = b;
	while (i < len)
	{
		g[i] = (unsigned char)0;
		i = i + 1;
	}
}
