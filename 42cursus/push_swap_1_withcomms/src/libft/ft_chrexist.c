#include "libft.h"

int	ft_chrexist(const char *s, int c)
{
	int	i;

	i = 0;
	while (c >= 256)
		c = c - 256;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (1);
		i = i + 1;
	}
	if (c == '\0')
		return (1);
	else
		return (0);
}
